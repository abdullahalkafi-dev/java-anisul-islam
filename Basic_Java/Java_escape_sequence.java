package Basic_Java;

public class Java_escape_sequence {
    public static void main (String[]args){
     System.out.println("Print with new line");
     System.out.print("Print with new line\n");  // with  newline escape sequence
     System.out.print("hello \t world\n");  // print with tab escape sequence
     System.out.print("\"Abdullah Al kafi\""); 
    }
}
