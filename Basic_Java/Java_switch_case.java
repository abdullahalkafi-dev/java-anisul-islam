package Basic_Java;

public class Java_switch_case {
 public static void main(String[]args){
    int a =2;
    switch(a){
     case 1: {
       System.out.println("The number is one");
       break;
     }
     case 2: {
        System.out.println("The number is two");
        break;
     }
     default:{
        System.out.println("I don't know the number");
     }
    }
 }

}
